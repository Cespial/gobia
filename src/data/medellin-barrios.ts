function getComunaTotalArea(comunaId: string): number {
  const barrios = barriosComunaMap[comunaId];
  if (!barrios) return 0;
  return barrios.reduce((sum, b) => sum + b.area, 0);
}

/**
 * Distributes a comuna's fiscal values proportionally by area to estimate
 * barrio-level values. Estrato is inherited directly from the comuna.
 *
 * @param comunaId - Comuna code (e.g., "01", "14")
 * @param barrioArea - Area of the barrio in m²
 * @param totalComunaArea - Optional total area of the comuna in m². If omitted, calculated from barriosComunaMap.
 */
export function getBarrioFiscalEstimate(
  comunaId: string,
  barrioArea: number,
  totalComunaArea?: number,
): BarrioFiscalEstimate | null {
  const fiscal = comunasFiscal[comunaId];
  if (!fiscal) return null;

  const total = totalComunaArea ?? getComunaTotalArea(comunaId);
  if (total === 0) return null;

  const ratio = barrioArea / total;

  return {
    predial: Math.round(fiscal.predial * ratio),
    ica: Math.round(fiscal.ica * ratio),
    poblacion: Math.round(fiscal.poblacion * ratio),
    estrato: fiscal.estrato,
  };
}
